using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace TankWars
{
    class Node
    {
        int x;
        int y;

        public int Y
        {
            get { return y; }
            set { y = value; }
        }

        public int X
        {
            get { return x; }
            set { x = value; }
        }
        String type;

        public String Type
        {
            get { return type; }
            set { type = value; }
        }
        String direction;

        public String Direction
        {
            get { return direction; }
            set { direction = value; }
        }
    




    }

